let button = document.querySelector("#snacks");

button.addEventListener("click", (event) => {
  let snackidea = document.querySelector(".content p");
  let min = Math.ceil(0);
  let max = Math.floor(snacklist.length - 1);
  let snackIndex = Math.floor(Math.random() * (max - min + 1) + min);

  snackidea.textContent = snacklist[snackIndex];

  snackidea.classList.remove("hidden");
});

